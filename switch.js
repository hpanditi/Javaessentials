let color = 'Orange'; 

switch (color) {
    case 'Red': 
        console.log('go red!');
        break;
    case 'Blue':
        console.log('go blue!');
        break;
    case 'Orange':
        console.log('go orange!'); 
        break;
    default:
        console.log("sorry, that's not a valid color");
}        